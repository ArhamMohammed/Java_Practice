package com.company;

public class CWH_04_literals {
    public static void main(String[] args) {
        int age=34;
        byte age1=34;
        char ch = 'A';
        float f1=5.6f;
        double d1=4.66;
        boolean b = false;
        System.out.println(age1);
        System.out.println(age);
        System.out.println(ch);
        System.out.println(f1);
        System.out.println(d1);
        System.out.println(b);
    }
}
